        console.warn(`Ëé∑ÂèñÈ¢òÁõÆÂéÜÂè≤ËÆ∞ÂΩïÂ§±Ë¥•:`, error)
      }
    }

    if (isViewingHistory.value) {
      isViewingHistory.value = false
