const formatAnswerWithOptions = (
  answer: string,
  options?: Record<string, string>,
  isMultipleChoice = false,
) => {
  if (!options) return answer

  if (isMultipleChoice) {
    return answer
      .split('')
      .map((key) => `${key}. ${options[key] || ''}`)
      .join(' + ')
  }
  return `${answer}. ${options[answer] || ''}`
}

